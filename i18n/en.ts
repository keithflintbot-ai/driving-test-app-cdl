// Recursively convert literal string types to string
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly string[]
      ? readonly string[]
      : T[K] extends Record<string, string>
        ? Record<string, string>
        : DeepStringify<T[K]>;
};

